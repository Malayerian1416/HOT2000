let crudType = ref(null);
let crudInfoCollection = reactive({
    name:null,
    collection: []
});
let temporaryCollection = reactive({name:null, value:null});
const flowbiteInit = async () => {
    const modals = flowbiteInstances.getInstances("Modal");
    const dropdowns = flowbiteInstances.getInstances("Dropdown");
    for (const [key] of Object.entries(modals))
        modals[key].init();
    for (const [key] of Object.entries(dropdowns))
        dropdowns[key].init();
}
watchEffect(async () => {
    await nextTick();
    await flowbiteInit();
});
onMounted(async () => {
    loading.value = false;
    maskElement();
    await fetchInfoCollections();
    initFlowbite();
    console.log(flowbiteInstances.getInstances("Modal"))
});
const showAlert = (show, type, message, timeout) => {
    alert.show = show;
    alert.type = type;
    alert.message = message;
    alert.timeout = timeout;
};
const fetchInfoCollections = async () =>{
    await axios.get(route("InfoCollection.index")).then(response => {
        if (crudType === 'create')
            showAlert(true, response.data.status, response.data.message, 3);
        infoCollections.value = response.data['infoCollections'];
    }).catch(error => {
        showAlert(true, 'error', error.message, 5);
    });
    await flowbiteInit();
}
const addCollectionItem = () => {
    if (temporaryCollection.name && temporaryCollection.value) {
        crudInfoCollection.collection.push({name: `info. ${temporaryCollection.name}`, value: temporaryCollection.value});
        temporaryCollection = {name:null, value:null};
    }

}
const setCrudInfoCollection = async (type, values = null) => {
    crudType.value = type;
    crudInfoCollection.id = values?.id ? values['id'] : null;
    crudInfoCollection.name = values?.name ? values['name'] : null;
    crudInfoCollection.collection = values?.collection ? values['collection_array'] : [];
    await nextTick();
    maskElement();
}
const createInfoCollection = (e) => {
    const button = e.currentTarget;
    buttonLoading(button, 'load');
    crudInfoCollection.collection = JSON.stringify(crudInfoCollection.collection);
    axios.post(route("InfoCollection.store"), crudInfoCollection).then(response => {
        buttonLoading(button, 'stop');
        showAlert(true, response.data.status, response.data.message, 3);
        fetchInfoCollections();
    }).catch(error => {
        buttonLoading(button, 'stop');
        showAlert(true, 'error', error.message, 5);
    });
};
const updateInfoCollection = (e) => {
    const button = e.currentTarget;
    buttonLoading(button, 'load');
    crudInfoCollection.collection = JSON.stringify(crudInfoCollection.collection);
    axios.put(route("InfoCollection.update",{id:crudInfoCollection.id}), crudInfoCollection).then(response => {
        buttonLoading(button, 'stop');
        showAlert(true, response.data.status, response.data.message, 3);
        fetchInfoCollections();
    }).catch(error => {
        buttonLoading(button, 'stop');
        showAlert(true, 'error', error.message, 5);
    });
};
const deleteInfoCollection = (e) => {
    const button = e.currentTarget;
    buttonLoading(button, 'load');
    axios.delete(route("InfoCollection.destroy",{id:crudInfoCollection.id})).then(response => {
        buttonLoading(button, 'stop');
        showAlert(true, response.data.status, response.data.message, 3);
        fetchInfoCollections();
    }).catch(error => {
        buttonLoading(button, 'stop');
        showAlert(true, 'error', error.message, 5);
    });
};
